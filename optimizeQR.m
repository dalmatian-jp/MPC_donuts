% optimizeQR.m

% Initial condition and reference
x0    = [0.0873; 0; 0; 0];
%x0    = [0.20; 0; 0; 0];
x_ref = [0; 0; 0; 0];

% MPC horizon/settings
N  = 10;
dt = 0.01;

%% 1) Define system parameters
systemParams = struct(...
    'm1',   28.00, ...
    'm2',   53.00, ...
    'L1',   0.90, ...
    'L2',   0.88, ...
    'I1',   9.21, ...
    'I2',   5.35, ...
    'com1', 0.58, ...
    'com2', 0.32, ...
    'g',    9.81);

% GA bounds for [Q,R]
%   params = [q1, q2, q3, q4, r1, r2]
QR_lb = [1100, 5000, 4000, 5500, 0.018, 0.009];
QR_ub = [4900, 7000, 6000, 7500, 0.222, 0.211];

opts = optimoptions('gamultiobj',...
    'PopulationSize', 50,...
    'MaxGenerations', 20,...
    'Display','iter',...
    'UseParallel', true,...
    'PlotFcn', @gaplotpareto);

[optParams, optObjs] = gamultiobj( ...
    @(p)paretoObjectiveEKF(p, x0, x_ref, N, dt, systemParams), ...
    6,[],[],[],[], QR_lb,QR_ub,[], opts);

disp('Optimal parameters:'); 
disp(optParams);

disp('Pareto Front [RMSE, Energy]:');
disp(optObjs);

figure; 
scatter(optObjs(:,1), optObjs(:,2),'filled');
xlabel('RMSE'); ylabel('Energy'); grid on;
title('Pareto Front');
